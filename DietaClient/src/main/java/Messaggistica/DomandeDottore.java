    private void estesaBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_estesaBtnMouseClicked
        try {
            DefaultTableModel model = (DefaultTableModel) tabellaDomande.getModel();  
            String msg = "ID Domanda: " + model.getValueAt(tabellaDomande.getSelectedRow(), 0);
            msg += "\nUtente: " + model.getValueAt(tabellaDomande.getSelectedRow(), 1);
            msg += "\n\nCategoria: " + model.getValueAt(tabellaDomande.getSelectedRow(), 2);
            msg += "\n\nDomanda:\n" + model.getValueAt(tabellaDomande.getSelectedRow(), 3);
            JOptionPane.showMessageDialog(null, msg, "Domanda", JOptionPane.INFORMATION_MESSAGE);    
        } catch(ArrayIndexOutOfBoundsException e) {
            JOptionPane.showMessageDialog(null, "Nessuna riga selezionata", "Domanda", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_estesaBtnMouseClicked

    private void rispondiBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_rispondiBtnMouseClicked
        DefaultTableModel model = (DefaultTableModel) tabellaDomande.getModel();
        String id = (String) model.getValueAt(tabellaDomande.getSelectedRow(), 0);
        String domanda = (String) model.getValueAt(tabellaDomande.getSelectedRow(), 3);
        FormRisposta fr = new FormRisposta(id, domanda);
        fr.setVisible(true);
        setVisible(false);
    }//GEN-LAST:event_rispondiBtnMouseClicked

