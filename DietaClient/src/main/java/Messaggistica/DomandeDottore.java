    public void visualizzaMessaggi() {
        ArrayList<String> domande = chat.getMessaggi();
        DefaultTableModel model = (DefaultTableModel) tabellaDomande.getModel();
        for(int i = 0; i < domande.size() / 4; i++) {
            model.addRow(new Object[] {
                domande.get(i*4),
                domande.get(i*4+1),
                domande.get(i*4+2),
                domande.get(i*4+3)
            });
        }
    }
    
