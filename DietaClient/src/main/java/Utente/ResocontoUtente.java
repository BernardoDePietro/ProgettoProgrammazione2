    private void avantiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_avantiMouseClicked
        String cambioData = calendario.getGiornoSuccessivo();
        if(calendario.comparaDate()) {
            data.setText(cambioData);
            String msg = vr.setProprietaMacronutrienti(cambioData);
            if(msg.equals("Resoconto non disponibile per questa data.")) {
                JOptionPane.showMessageDialog(null, msg, "Resoconto non disponibile", JOptionPane.WARNING_MESSAGE);
                impostaProprieta(0); //SETTA TUTTI I VALORI NULL
                nessun.setVisible(true);
            } else {
                nessun.setVisible(false);
                vr.setProprietaPasti(cambioData, "colazione");
                vr.setProprietaPasti(cambioData, "spuntino");
                vr.setProprietaPasti(cambioData, "pranzo");
                vr.setProprietaPasti(cambioData, "cena");
                impostaProprieta(1);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Non è possibile visualizzare il resoconto di giorni successivi ad oggi.", "Cambio data", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_avantiMouseClicked

    private void indietroMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_indietroMouseClicked
        String cambioData = calendario.getGiornoPrecedente();
        data.setText(cambioData);
        String msg = vr.setProprietaMacronutrienti(cambioData);
        if(msg.equals("Resoconto non disponibile per questa data.")) {
            JOptionPane.showMessageDialog(null, msg, "Resoconto non disponibile", JOptionPane.WARNING_MESSAGE);
            impostaProprieta(0); //SETTA TUTTI I VALORI NULL
            nessun.setVisible(true);
        } else {
            nessun.setVisible(false);
            vr.setProprietaPasti(cambioData, "colazione");
            vr.setProprietaPasti(cambioData, "spuntino");
            vr.setProprietaPasti(cambioData, "pranzo");
            vr.setProprietaPasti(cambioData, "cena");
            impostaProprieta(1);
        }
    }//GEN-LAST:event_indietroMouseClicked

