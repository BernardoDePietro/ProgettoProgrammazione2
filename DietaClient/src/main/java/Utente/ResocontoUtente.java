    private void impostaProprieta(int flag) {
        if(flag == 0) {
            jPanel1.setVisible(false);
        } else {
            jPanel1.setVisible(true);
            calorieLabel.setText("Hai assunto " + vr.getCalorieAssunte() + " di " + vr.getCalorieTotali() + "kcal");
            carboidratiLabel.setText("Hai assunto " + vr.getCarboidratiAssunti() + " di " + vr.getCarboidratiTotali() + "g");
            proteineLabel.setText("Hai assunto " + vr.getProteineAssunte() + " di " + vr.getProteineTotali() + "g");
            grassiLabel.setText("Hai assunto " + vr.getGrassiAssunti() + " di " + vr.getGrassiTotali() + "g");
            colazioneLabel.setText(vr.getColazioneAssunta() + "/" + vr.getColazioneTotale() + "kcal");
            spuntinoLabel.setText(vr.getSpuntinoAssunto() + "/" + vr.getSpuntinoTotale() + "kcal");
            pranzoLabel.setText(vr.getPranzoAssunto() + "/" + vr.getPranzoTotale() + "kcal");
            cenaLabel.setText(vr.getCenaAssunta() + "/" + vr.getCenaTotale() + "kcal");
            impostaStato();
        }
    }
    
    private void impostaStato() {
        caloriePanel.setBackground(vr.statoMacronutrienti(vr.getCalorieTotali(), vr.getCalorieAssunte()));
        carboidratiPanel.setBackground(vr.statoMacronutrienti(vr.getCarboidratiTotali(), vr.getCarboidratiAssunti()));
        proteinePanel.setBackground(vr.statoMacronutrienti(vr.getProteineTotali(), vr.getProteineAssunte()));
        grassiPanel.setBackground(vr.statoMacronutrienti(vr.getGrassiTotali(), vr.getGrassiAssunti()));
        colazionePanel.setBackground(vr.statoPasti(vr.getColazioneTotale(), vr.getColazioneAssunta()));
        spuntinoPanel.setBackground(vr.statoPasti(vr.getSpuntinoTotale(), vr.getSpuntinoAssunto()));
        pranzoPanel.setBackground(vr.statoPasti(vr.getPranzoTotale(), vr.getPranzoAssunto()));
        cenaPanel.setBackground(vr.statoPasti(vr.getCenaTotale(), vr.getCenaAssunta()));
    }
    
